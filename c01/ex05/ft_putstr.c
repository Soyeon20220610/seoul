#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char	*str)
{
	char	c;

	c = '\0';
	while (*str != c)
	{
		ft_putchar(*str);
		str++;
	}
}
